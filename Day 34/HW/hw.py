def sum_of_two_numbers(num1, num2):
    print(num1 + num2)
sum_of_two_numbers(5, 3)  

def find_maximum(numbers):
    print(max(numbers))
numbers = [3, 5, 7, 2, 8]
find_maximum(numbers) 

def welcome_user(name):
    print(f"Welcome {name}!")
welcome_user("John")  

#ფუნქცია (Function) – კოდის ჯგუფი, რომელიც ასრულებს გარკვეულ ქმედებას. მაგალითად, def greet(): წარმოადგენს ფუნქციას.

#პარამეტრი (Parameter) – ცვლადი, რომელიც ფუნქციას გადაეცემა. მაგალითად, name ფუნქციაში def greet(name): არის პარამეტრი.

#არგუმენტი (Argument) – კონკრეტული მნიშვნელობა, რომელიც გადაეცემა ფუნქციას. მაგალითად, greet("Alice")–ში "Alice" არის არგუმენტი.

#def (Keyword) – კლავიატურა, რომელიც გამოიყენება ფუნქციის განსაზღვრისთვის.

#Keyword – რეზერვირებული სიტყვები, როგორიცაა def, if, for, რომლებიც სპეციალური მნიშვნელობა აქვთ Python-ში.

#Code Block – კოდის ჯგუფი, რომელიც ერთიანად მუშაობს. Python-ში კოდის ბლოკი განსაზღვრულია ინდენტაციით (გადატანის ხაზებით).




